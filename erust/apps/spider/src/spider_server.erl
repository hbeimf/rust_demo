% --------------------------------------------------------------------
% Function: init/1
% Description: Initiates the server
% Returns: {ok, State}          |
%          {ok, State, Timeout} |
%          ignore               |
%          {stop, Reason}
% --------------------------------------------------------------------
init([]) ->
	erlang:send_after(?TIMER, self(), run),
	% Port = start_rs_server(),
	% State = #state{code = Code},
	State = [],
	{ok, State}.

% --------------------------------------------------------------------
% Function: handle_call/3
% Description: Handling call messages
% Returns: {reply, Reply, State}          |
%          {reply, Reply, State, Timeout} |
%          {noreply, State}               |
%          {noreply, State, Timeout}      |
%          {stop, Reason, Reply, State}   | (terminate/2 is called)
%          {stop, Reason, State}            (terminate/2 is called)
% --------------------------------------------------------------------
handle_call(_Request, _From, State) ->
	% ?LOG(Request),
	Reply = ok,
	{reply, Reply, State}.

% --------------------------------------------------------------------
% Function: handle_cast/2
% Description: Handling cast messages
% Returns: {noreply, State}          |
%          {noreply, State, Timeout} |
%          {stop, Reason, State}            (terminate/2 is called)
% --------------------------------------------------------------------
handle_cast(run, State) ->
	?LOG(run),
	Codes = code_list(),
	fetch_code(Codes),
	% ll:print(),
	{noreply, State};
handle_cast(_Msg, State) ->
	% ?LOG(Msg),
	{noreply, State}.

% --------------------------------------------------------------------
% Function: handle_info/2
% Description: Handling all non call/cast messages
% Returns: {noreply, State}          |
%          {noreply, State, Timeout} |
%          {stop, Reason, State}            (terminate/2 is called)
% --------------------------------------------------------------------
handle_info(run, State) ->
	% ?LOG(Info),
	Codes = code_list(),
	fetch_code(Codes),
	% ll:print(),
	{noreply, State};
handle_info(_Info, State) ->
	% ?LOG(Info),
	{noreply, State}.

% --------------------------------------------------------------------
% Function: terminate/2
% Description: Shutdown the server
% Returns: any (ignored by gen_server)
% --------------------------------------------------------------------
terminate(_Reason, _State) ->
	% stop_rs_server(),
	ok.

% --------------------------------------------------------------------
% Func: code_change/3
% Purpose: Convert process state when code is changed
% Returns: {ok, NewState}
% --------------------------------------------------------------------
code_change(_OldVsn, State, _Extra) ->
	{ok, State}.


%% priv fun

fetch_code([]) ->
	ok; 
fetch_code([CodeInfo|Tail]) -> 
	{Code, _, _} = CodeInfo,
	% ?LOG(Code),
	Url = lists:concat(["http://quotes.money.163.com/service/chddata.html?code="
		, glib:to_str(Code)
		, "&start=20190101&end="
		, glib:date_str("ymd")]),
	% ?LOG(Url),
	Res = glib:http_get(Url),
	% ?LOG(Res),
	case parse_res(Res) of 
		false -> 
			?LOG({fail, Code}),
			ok;
		Rows ->
			% ?LOG(Rows),
			% ?LOG({init, Code}),
			sync_server:start_code(CodeInfo, Rows),
			ok
	end, 

	fetch_code(Tail).

parse_res({error, _Reason}) ->
	false;
parse_res(Body) ->
	% ?LOG(Body),
	Body1 = glib:to_str(Body),
	Lines = glib:explode(Body1, "\r\n"),
	% ?LOG(Lines),
	case Lines of 
		[] -> 
			false;
		[_|LineList] -> 
			Rows = lists:map(fun(Line) -> 
				% ?LOG(Line),
				Data = glib:explode(Line, ","),
				% ?LOG(Data),
				% [
				% 	{<<"date">>, lists:nth(1, Data)}
				% 	,{<<"price">>, lists:nth(4, Data)}
				% 	,{<<"c_num">>, lists:nth(12, Data)}
				% ]
				{lists:nth(1, Data), lists:nth(4, Data), lists:nth(12, Data)}
			end, LineList),
			Rows
	end.




