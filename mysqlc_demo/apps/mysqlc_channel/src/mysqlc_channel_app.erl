-module(mysqlc_channel_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
	%% 初始化连接
	mysqlc_channel_pool:init_pool(),
    mysqlc_channel_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

